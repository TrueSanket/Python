"""
Sorting integers, floating numbers and letters
"""

# List of Integers
numbers = [1, 3, 4, 2]
# Sorting list of Integers
numbers.sort()
print(numbers)

# List of Floating point numbers
floating = [2.01, 2.00, 3.67, 3.28, 1.68]
# Sorting list of Floating point numbers
floating.sort()
print(floating)

# List of alphabets
alphabets = ['h','f','d','s','b','z','a','l']
# Sorting list of strings
alphabets.sort()
print(alphabets)